# \b => back space
# \newline => escape new line + \
# \\ => Escape BACK
# \' => Escape Single Quotes
# \" => Escape Double Quotes
# \n => line feed
# \r => carriage return
# \t => Horizontal Tab
# \xhh => Hex Value

# back space
print('hello\bshadi') # will remove the o

# newline
print("hello \
    shadi\
        in python")

# Escape back Slash 
print('i love you\\')

# Escape Single + double quote
print('i love \'shadi\' ' )

# Line Feed
print('hello \n world')

# Carriage Return
print('123456\rWorld') #replaces characters

# Horizontal Tap
print('hello\tworld')

# Hex value
print('\x4f\x73')